        <h2 class="title title--border">¿Cuáles son los tipos de depresión?</h2>

        <p class="about__paragraph"><li><strong>La depresión mayor </strong> tiene un origen más biológico o endógeno, con un mayor componente genético y menor influencia de factores externos. Puede aparecer de manera recurrente y, en algunos casos, guarda una cierta relación con la estación del año.</li></p>
        <p class="about__paragraph"><li>En contraposición, <strong>la depresión reactiva,</strong> causada por una mala adaptación a circunstancias ambientales estresantes.</li></p>
        <p class="about__paragraph"><li><strong>La distimia </strong>antiguamente conocida como neurosis depresiva, que se caracteriza por un cuadro depresivo de intensidad menor a los anteriores, de evolución crónica (más de dos años), sin periodos asintomáticos y con sentimientos de incapacidad y somatizaciones. Este último tipo de depresión parece guardar una relación más estrecha con la forma de ser y con el estrés prolongado.</li></p>
        <p class="about__paragraph"><li>Por último, existe un tipo de <strong> depresión </strong> denominada <strong> enmascarada, </strong>  que en vez de manifestarse con los síntomas ya referidos, aparece como molestias orgánicas -somatizaciones- o cambios en la conducta.</li></p>

    <div class="about__course">
        <h2 class="title title--border">¿Cuáles son los tipos de trastornos de ansiedad?</h2>

        <p class="about__paragraph"><li><strong>Trastorno de ansiedad generalizada: </strong> Las personas con este trastorno se preocupan por problemas comunes como la salud, el dinero, el trabajo y la familia. Pero sus preocupaciones son excesivas y las tienen casi todos los días durante al menos 6 meses</li></p>
        <p class="about__paragraph"><li><strong>Trastorno de pánico: </strong>Las personas con trastorno de pánico sufren de ataques de pánico. Estos son repentinos y repetidos momentos de miedo intenso sin haber un peligro aparente. Los ataques se producen rápidamente y pueden durar varios minutos o más</li></p>
        <p class="about__paragraph"><li><strong>Fobias: </strong>Las personas con fobias tienen un miedo intenso a algo que representa poco o ningún peligro real. Su miedo puede ser por arañas, volar, ir a lugares concurridos o estar en situaciones sociales (conocida como ansiedad social)
        </li></p>

    </div>

